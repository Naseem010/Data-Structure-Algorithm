class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        Node *curr=head;
        priority_queue <int, vector<int>, greater<int> > pq;
        while(curr!=NULL){
            pq.push(curr->data);
            curr=curr->next;
        }
        Node *prev=new Node(0);
        Node *ans=prev;
        while(!pq.empty()){
            int value=pq.top();
            pq.pop();
           Node *node=new Node(value);
           prev->next=node;
           prev=prev->next;
            
        }
        return ans->next;
    }
};


